const express = require('express')
const app = express()
// const moment = require('moment');
require('dotenv').config()
const port = process.env.PORT
require('./db/mongoose')
app.use(express.json())
const momRouter = require('./routers/mom')
const articlesRouter = require('./routers/articles')
const adminRouter = require('./routers/admin')
const doctorRouter = require('./routers/doctor')
const postsRouter = require('./routers/posts')
const Appointment = require('./routers/momAppointment')
const availableAppointment = require('./routers/doctorAvailableAppointments')
const surveyRouter = require('./routers/survey')
const Contact = require('./routers/contactUs')
app.use(momRouter)
app.use(articlesRouter)
app.use(adminRouter)
app.use(doctorRouter)
app.use(postsRouter)
app.use(Appointment)
app.use(availableAppointment)
app.use(surveyRouter)
app.use(Contact)
app.listen(port,()=>{
    console.log('Server is running')
})